
public class sweek01 {
	public static void main(String [] args){
		int[] arry=new int[11];//11크기의 arry 배열 선언&생성
		int c=0;// 합을 구하기위한 변수
		for(int a=0;a<arry.length;a++){ //배열에 값을 넣기위한 for문
			arry[a]=a; //arry 배열에 값을 넣는다.
		}
		for(int z=0;z<arry.length;z++){//합을 구하기위한 for
			c+=arry[z];// 배열의 합을 더한다.
		}
      System.out.print((3/2));//배열의 합 출력
	}

}

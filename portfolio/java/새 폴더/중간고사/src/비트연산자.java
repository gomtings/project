
public class 비트연산자 {
	public static void main(String [] args){
		int a=9;
		int b=13;
		
		System.out.println(  ~ b); // bit not // ==1010 == 
		System.out.println(a & b); // bit AND   ==1010 ==
		System.out.println(a ^ b); // bit XOR   ==1010 ==
		System.out.println(a | b); //bit OR    ==1010 ==
		System.out.println(a<< b);  //비트왼쪽이동  ==1010 ==
		System.out.println(a>> b);  //비트 오른쪽 이동==1010 ==
		System.out.println(a>>>b);  // 비트 오른쪽이동 ==1010 ==
		/*비트 연산자 
		 * 
		 */
	}

}
